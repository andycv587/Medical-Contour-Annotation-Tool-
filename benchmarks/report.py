import csv
import json
import os
from typing import List, Optional


def write_report(output_dir: str, rows: List[dict], config: dict, *, benchmark_kind: str = "", oracle_prompt: bool = False, warnings: Optional[List[str]] = None) -> str:
    warnings = warnings or []
    report_path = os.path.join(output_dir, "report.md")
    with open(report_path, "w", encoding="utf-8") as f:
        f.write("# Benchmark Report\n\n")
        f.write("This report contains only results generated by the benchmark command. Synthetic smoke results are not real-data performance claims.\n\n")
        if benchmark_kind:
            f.write(f"- Benchmark kind: `{benchmark_kind}`\n")
        if oracle_prompt:
            f.write("- Prompt setting: `oracle-prompt benchmark`\n")
        else:
            f.write("- Prompt setting: `user-prompt or automatic benchmark`\n")
        categories = sorted({str(row.get("result_category", "")) for row in rows if row.get("result_category")})
        if categories:
            f.write("- Result categories: " + ", ".join(f"`{item}`" for item in categories) + "\n")
        f.write("\n")
        if warnings:
            f.write("## Warnings\n\n")
            for warning in warnings:
                f.write(f"- {warning}\n")
            f.write("\n")
        f.write("## Configuration\n\n")
        f.write("```json\n")
        f.write(json.dumps(config, indent=2, sort_keys=True))
        f.write("\n```\n\n")
        f.write("## Cases\n\n")
        f.write("| case_id | dataset | category | selected_backend | oracle_prompt | dice | iou | object_f1 | runtime_sec |\n")
        f.write("|---|---|---|---|---:|---:|---:|---:|---:|\n")
        for row in rows:
            f.write(
                f"| {row.get('case_id')} | {row.get('dataset_name', '')} | {row.get('result_category', '')} | {row.get('selected_backend')} | "
                f"{row.get('oracle_prompt', False)} | "
                f"{_fmt(row.get('dice'))} | {_fmt(row.get('iou'))} | {_fmt(row.get('object_f1'))} | {_fmt(row.get('runtime_sec'))} |\n"
            )
    return report_path


def write_metrics_csv(path: str, rows: List[dict]) -> None:
    if not rows:
        return
    fields = sorted({k for row in rows for k in row.keys()})
    with open(path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fields)
        writer.writeheader()
        writer.writerows(rows)


def _fmt(value):
    if value is None or value == "":
        return "NA"
    if isinstance(value, float):
        return f"{value:.4f}"
    return str(value)
